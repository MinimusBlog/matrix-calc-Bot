import numpy as np

def addition(a, b): #Сложение
    return np.add(a, b)

def substract(a, b): #Вычитание
    return np.subtract(a, b)

def multiply(a, b): #Умножение
    return np.multiply(a, b)

def divide(a, b): #Деление
    return np.divide(a, b)

def power(a, b): #Возведение в степень
    return np.power(a, b)

def sqrt(a): #Квадратный корень
    return np.sqrt(a)

def log(a): #Логарифм
    return np.log(a)

def sin(a): #Синус
    return np.sin(a)

def cos(a): #Косинус
    return np.cos(a)

def tan(a): #Тангенс
    return np.tan(a)
